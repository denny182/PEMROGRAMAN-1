//ini adalah nama class yang harus sesuai dengan nama filenya. Praktikum.java
public class Praktikum{
//dibawah ini adalah method utama yang akan dieksekusi oleh java
	public static void main(String args[]){
		int count = 1; //Pemberian nilai 1 pada variabel count dan bertipe int. 
		while(count < 5){ //pernyataan untuk mengeksekusi perintah dan batas dari eksekusi tersebut.
			System.out.println("Count is equal to " + count);//output dari program
			count++; //proses pengulangan variabel count
		}
	}
}