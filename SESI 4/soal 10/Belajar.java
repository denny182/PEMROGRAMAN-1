//ini adalah nama class yang harus sesuai dengan nama filenya. Belajar.java
public class Belajar{
//dibawah ini adalah method utama yang akan dieksekusi oleh java
	public static void main(String[] args){
		int num=11; //pemberian nilai 11 pada variabel num dan bertipe int
		if(num > 5) //Branching pada if dengan pernyataan num > 5
			System.out.println("Number is Greater Than 5");//output dari program
		else //proses eksekusi program dalam pemilihan
			System.out.println("Number is less Than 5");//output dari program
	}
}
	
