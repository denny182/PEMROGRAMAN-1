//ini adalah nama class yang harus sesuai dengan nama filenya. Praktikum.java
public class Praktikum{
	//dibawah ini adalah method utama yang akan dieksekusi oleh java
	public static void main(String[] args){
		int a=4,b=6;//pemberian nilai pada variabel a=4 dan b=6 dan bertipe int
		int num=a | b;//perbandingan variabel dengan menggunakan OR
		if(num==4) //Branching
			System.out.println("value is 4");//output dari program
		else if(num==5)//Branching
			System.out.println("value is 5");//output dari program
		else if(num==6)//Branching
			System.out.println("value is 6");//output dari program
		else if(num==7)//Branching
			System.out.println("value is 7");//output dari program
	}
}